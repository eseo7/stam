/* ============================================================================
 * STAM 메뉴구조/화면목록 게시판 — Local Core DB v2 CRUD
 * ----------------------------------------------------------------------------
 * menuScreens store(v2) 데이터의 상세/등록/수정/삭제를 단일 Drawer(#msv2-dw)로 처리.
 *   - 모드: register / detail / edit
 *   - 상세 탭: 기본 정보 / 연결 정보 / 검토 이력 / 변경 이력
 *   - Footer(상세): 삭제 · 전체 보기 · 수정
 *   - custom select: STAM.customSelect (native 파란 dropdown 없음)
 *   - soft delete만 허용 (hard delete 금지)
 *   - 변경이력: artifactChanges store
 *
 * Firebase/Firestore/API 미사용.
 * ==========================================================================*/
(function () {
  'use strict';

  var PID   = 'proto-proj-001';
  var STORE = 'menuScreens';
  var BY    = 'prototype-user';

  function $(id) { return document.getElementById(id); }
  function nowIso() { return (new Date()).toISOString(); }
  function esc(s) {
    return String(s == null ? '' : s).replace(/[&<>"]/g, function (c) {
      return { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;' }[c];
    });
  }
  function dpart(iso) { return String(iso || '').replace('T', ' ').slice(0, 10); }
  function changeId(id, type) { return id + '-' + type + '-' + Date.now(); }

  // ── Custom select config — 공통 stam-cs-* 기준 ───────────────────────────
  var CS_CFG = {
    selectSelector:   'select.stam-form-input',
    nativeMarkerAttr: 'data-msv2-cs',
    uidPrefix:        'msv2cs',
    wrapClass:        'stam-cs',
    triggerClass:     'stam-cs-trigger',
    valClass:         'stam-cs-value',
    caretClass:       'stam-cs-icon',
    panelClass:       'stam-cs-menu',
    optClass:         'stam-cs-opt',
    checkClass:       'stam-cs-check',
    otextClass:       'stam-cs-otext',
    nativeClass:      'stam-cs-native',
    flipContainer:    '.stam-drawer-body',
    openClass:        'is-open',
    upClass:          'is-up',
    openSelector:     '.stam-cs.is-open'
  };

  function resetSelects() {
    var form = document.querySelector('#msv2-dw .stam-drawer-form-area');
    if (!form) return;
    form.querySelectorAll('.stam-cs').forEach(function (w) {
      var nat = w.querySelector('select');
      if (nat) { nat.classList.remove('stam-cs-native'); nat.removeAttribute('data-msv2-cs'); w.parentNode.insertBefore(nat, w); }
      w.remove();
    });
  }
  function enhanceSelects() {
    var form = document.querySelector('#msv2-dw .stam-drawer-form-area');
    if (form && window.STAM && window.STAM.customSelect) window.STAM.customSelect.init(form, CS_CFG);
  }

  // ── 값 한글화 ─────────────────────────────────────────────────────────────
  var STATUS_KO = {
    draft: '작성중', active: '진행중', confirmed: '확정',
    hold: '보류', reviewing: '검토중', deleted: '삭제됨'
  };
  var REVIEW_KO = {
    'Review Needed': '검토 필요', 'In Review': '검토중', 'Approved': '승인완료'
  };

  function statusKo(s)  { return STATUS_KO[s]  || s || '작성중'; }
  function reviewKo(s)  { return REVIEW_KO[s]  || s || '검토 필요'; }
  function sourceKo(rec) {
    if (rec.sourceType === 'Requirement Import') return '요구사항 가져오기';
    if (rec.sourceType === 'manual')             return '직접 등록';
    return rec.importBatchId ? '요구사항 가져오기' : '직접 등록';
  }
  function isAutoGenerated(rec) {
    return !!(rec && (rec.sourceType === 'Requirement Import' || rec.importBatchId));
  }

  function statusChip(s) {
    var labels = {
      draft: '작성중', active: '진행중', confirmed: '확정',
      hold: '보류', reviewing: '검토중'
    };
    var tones = {
      draft: '', active: 'is-brand', confirmed: 'is-pass',
      hold: 'is-fail', reviewing: 'is-warn'
    };
    var label = labels[s] || labels.draft;
    var tone = tones[s] || '';
    var cls = 'stam-detail-chip' + (tone ? ' ' + tone : '');
    return '<span class="' + cls + '">' + label + '</span>';
  }

  // ── Drawer 상태 ───────────────────────────────────────────────────────────
  var scrim     = $('msv2-scrim');
  var drawer    = $('msv2-dw');
  var currentId = null;

  // 기존 static drawer(#msl-dw-*)와 scrim(#msl-scrim) 닫기
  function closeStaticDrawers() {
    document.querySelectorAll('.msl-drawer.open').forEach(function (d) { d.classList.remove('open'); });
    var mslScrim = $('msl-scrim');
    if (mslScrim) mslScrim.classList.remove('show');
  }

  function openDrawer(mode) {
    closeStaticDrawers();
    if (scrim)  scrim.classList.add('show');
    if (drawer) { drawer.classList.add('open'); drawer.setAttribute('data-mode', mode); }
    var tabs = $('msv2-tabs');
    if (tabs) tabs.classList.toggle('is-hidden', mode !== 'detail');
    if (mode === 'detail') setActiveTab('info');
  }

  function closeDrawer() {
    resetSelects();
    if (scrim)  scrim.classList.remove('show');
    if (drawer) { drawer.classList.remove('open'); drawer.setAttribute('data-mode', 'detail'); }
    currentId = null;
  }

  function setActiveTab(tab) {
    document.querySelectorAll('.stam-drawer-tab').forEach(function (b) {
      b.classList.toggle('on', b.getAttribute('data-msv2-tab') === tab);
    });
    document.querySelectorAll('.stam-drawer-tab-panel').forEach(function (p) {
      p.classList.toggle('is-hidden', p.getAttribute('data-tab') !== tab);
    });
  }

  // ── DB ────────────────────────────────────────────────────────────────────
  function db() { return window.STAM_CORE && window.STAM_CORE.db; }

  // ── 상세 탭 — STAM.detailDrawer (PR #256 공통 renderer) ─────────────────
  function mountDetailTab(el, config) {
    if (!el) return;
    if (window.STAM && window.STAM.detailDrawer) {
      window.STAM.detailDrawer.mount(el, config);
      return;
    }
    el.textContent = '—';
  }

  function statusChipItems(s) {
    var map = {
      draft:     { label: '작성중', tone: 'warn' },
      active:    { label: '진행중', tone: 'brand' },
      confirmed: { label: '확정',   tone: 'pass' },
      hold:      { label: '보류',   tone: 'fail' },
      reviewing: { label: '검토중', tone: 'warn' },
    };
    var v = map[s] || map.draft;
    return [{ label: v.label, tone: v.tone }];
  }

  function detailField(label, value, opts) {
    opts = opts || {};
    var f = { label: label };
    if (opts.full) f.full = true;
    if (opts.type) f.type = opts.type;
    if (value != null && String(value) !== '') {
      f.value = value;
      return f;
    }
    if (opts.type) {
      f.value = opts.value != null ? opts.value : '';
      if (opts.emptyText) f.emptyText = opts.emptyText;
      return f;
    }
    if (opts.important) {
      f.emptyText = opts.emptyText || '미지정 · 수정에서 입력 필요';
      f.value = '';
      return f;
    }
    if (opts.emptyText) {
      f.emptyText = opts.emptyText;
      f.value = '';
      return f;
    }
    return null;
  }

  function pushField(fields, field) {
    if (field) fields.push(field);
  }

  function buildInfoConfig(rec) {
    var isAuto = isAutoGenerated(rec);
    var missing = [];
    if (!rec.screenName) missing.push('화면명');
    if (!rec.menuName)   missing.push('메뉴명');
    if (!rec.screenType) missing.push('화면 유형');
    if (!rec.lv1)        missing.push('LV1');
    if (!rec.routePath)  missing.push('화면 경로');

    var summaryFields = [
      { label: '생성 방식', value: sourceKo(rec) },
      { label: '현재 상태', type: 'chips', value: statusChipItems(rec.status) },
    ];
    if (missing.length) {
      summaryFields.push({
        label: '보완 필요',
        type: 'chips',
        value: missing.map(function (m) { return { label: m, tone: 'warn' }; }),
        full: true,
      });
    } else {
      summaryFields.push({
        label: '완성도',
        type: 'chips',
        value: [{ label: '필수 항목 입력 완료', tone: 'pass' }],
      });
    }

    var basicFields = [];
    pushField(basicFields, detailField('메뉴/화면 ID', rec.id));
    pushField(basicFields, detailField('화면명', rec.screenName, { important: true }));
    pushField(basicFields, detailField('메뉴명', rec.menuName, { important: true }));
    pushField(basicFields, detailField('LV1', rec.lv1, { important: true }));
    pushField(basicFields, detailField('LV2', rec.lv2));
    pushField(basicFields, detailField('LV3', rec.lv3));
    pushField(basicFields, detailField('화면 유형', rec.screenType, { important: true }));
    pushField(basicFields, detailField('채널', rec.channel));
    pushField(basicFields, detailField('화면 경로', rec.routePath, { important: true }));
    pushField(basicFields, detailField('담당자', rec.owner));
    pushField(basicFields, { label: '작성 상태', type: 'chips', value: statusChipItems(rec.status) });
    pushField(basicFields, detailField('검토 상태', reviewKo(rec.reviewStatus)));
    pushField(basicFields, detailField('최초 등록일', dpart(rec.createdAt)));
    pushField(basicFields, detailField('최종 수정일', dpart(rec.updatedAt)));

    var sections = [
      { title: '요약', fields: summaryFields },
      { title: '기본 정보', fields: basicFields },
    ];

    if (rec.description) {
      sections.push({
        title: '화면 설명',
        fields: [{ label: '내용', type: 'note', value: rec.description, full: true }],
      });
    }

    sections.push({
      title: '작성 안내',
      fields: [{
        label: '안내',
        type: 'note',
        value: isAuto
          ? '요구사항을 바탕으로 생성된 메뉴/화면 초안입니다. 화면명, 메뉴 계층, 화면 유형, 경로를 확인한 뒤 수정에서 보완하세요.'
          : '직접 등록된 메뉴/화면입니다. 관련 요구사항과 화면설계서 연결 정보를 확인하세요.',
        full: true,
      }],
    });

    return { sections: sections };
  }

  function buildLinkConfig(rec) {
    var links = [
      { label: '연결 요구사항', id: rec.requirementId },
      { label: '연결 기능정의', id: rec.functionId },
      { label: '연결 화면설계서', id: rec.screenSpecificationId },
    ];
    var linkN = links.filter(function (l) { return !!l.id; }).length;

    var sections = [
      {
        title: '연결 요약',
        fields: [{ label: '연결 건수', value: String(linkN) + '건' }],
      },
      {
        title: '연결 산출물',
        fields: links.map(function (l) {
          return {
            label: l.label,
            value: l.id || '',
            emptyText: '연결 없음',
          };
        }),
      },
    ];

    if (linkN === 0) {
      sections.push({
        title: '안내',
        fields: [{
          label: '연결 정보',
          type: 'note',
          value: '연결된 요구사항, 기능정의, 화면설계서가 없습니다. 수정에서 연결 정보를 입력할 수 있습니다.',
          full: true,
        }],
      });
    }

    return { sections: sections };
  }

  function buildReviewConfig() {
    return {
      sections: [{
        title: '검토 이력',
        emptyText: '검토 이력이 없습니다.',
        fields: [],
      }],
    };
  }

  function historyLine(c) {
    var who = (c.by && c.by !== BY) ? c.by : '작업자';
    var what = c.changeType === 'create' ? '메뉴/화면을 등록했습니다.'
      : c.changeType === 'delete' ? '메뉴/화면을 삭제했습니다.'
      : c.changeType === 'status'
        ? ('상태를 변경했습니다. ' + statusKo(c.before) + ' → ' + statusKo(c.after))
        : c.changeType === 'update' ? '내용을 수정했습니다.'
        : (c.changeType || '변경');
    return who + ' — ' + what + ' (' + dpart(c.at) + ')';
  }

  function buildHistoryConfig(items) {
    if (!items || !items.length) {
      return {
        sections: [{
          title: '변경 이력',
          emptyText: '변경 이력이 없습니다.',
          fields: [],
        }],
      };
    }
    return {
      sections: [{
        title: '변경 이력',
        fields: [{
          label: '이력',
          type: 'list',
          value: items.map(historyLine),
          full: true,
        }],
      }],
    };
  }

  function renderDetail(rec) {
    var infoEl = $('msv2-tab-info');
    if (infoEl) mountDetailTab(infoEl, buildInfoConfig(rec));

    var linkEl = $('msv2-tab-link');
    if (linkEl) mountDetailTab(linkEl, buildLinkConfig(rec));

    var reviewEl = $('msv2-tab-review');
    if (reviewEl) mountDetailTab(reviewEl, buildReviewConfig());

    var histEl = $('msv2-tab-history');
    if (!histEl) return;

    var d = db();
    if (!d) {
      mountDetailTab(histEl, buildHistoryConfig([]));
      return;
    }

    d.listRecords('artifactChanges', PID, { includeDeleted: true }).then(function (all) {
      var items = (all || []).filter(function (c) { return c.artifactId === rec.id; })
        .sort(function (a, b) { return String(b.at || '').localeCompare(String(a.at || '')); });
      mountDetailTab(histEl, buildHistoryConfig(items));
    }).catch(function () {
      mountDetailTab(histEl, {
        sections: [{
          title: '변경 이력',
          emptyText: '변경 이력을 불러올 수 없습니다.',
          fields: [],
        }],
      });
    });
  }

  // ── 헤더 ─────────────────────────────────────────────────────────────────
  function setHeader(rec, mode) {
    var head = $('msv2-head');
    if (!head) return;
    var badge = rec ? esc(rec.id) : 'NEW';

    var title;
    if (!rec) {
      title = '새 메뉴/화면 등록';
    } else if (rec.screenName) {
      title = esc(rec.screenName);
    } else if (rec.menuName) {
      title = esc(rec.menuName) + ' <span class="stam-drawer-title-hint">(화면명 미지정)</span>';
    } else if (rec.requirementId) {
      title = esc(rec.requirementId) + ' 기반 화면 초안';
    } else {
      title = '화면명 미지정';
    }

    var rowSubtitle = '';
    var statusInRow = '';
    var metaHtml = '';

    if (mode === 'register' && !rec) {
      rowSubtitle = '<span class="stam-drawer-head-subtitle">메뉴/화면 등록</span>';
      metaHtml = '<span class="stam-detail-chip is-warn">작성중</span>' +
        '<span class="stam-drawer-meta-source">등록 후 ID가 자동 부여됩니다</span>';
    } else if (rec) {
      statusInRow = statusChip(rec.status);
      metaHtml = '<span class="stam-drawer-meta-source">' + esc(sourceKo(rec)) + '</span>';
    }

    var noticeHtml = '';
    if (mode === 'edit' && isAutoGenerated(rec)) {
      noticeHtml = '<div class="stam-edit-notice">자동 생성된 초안입니다. 화면명과 메뉴 계층을 검토한 뒤 저장하세요.</div>';
    }

    head.innerHTML =
      '<div class="stam-drawer-head-row">' +
        '<span class="stam-drawer-head-badge">' + badge + '</span>' +
        rowSubtitle +
        statusInRow +
        '<button class="stam-drawer-close" id="msv2-close-btn" aria-label="닫기">' +
          renderStamIcon('close', { className: 'is-sm' }) +
        '</button>' +
      '</div>' +
      '<div class="stam-drawer-title">' + title + '</div>' +
      (metaHtml ? '<div class="stam-drawer-meta">' + metaHtml + '</div>' : '') +
      noticeHtml;

    var btn = $('msv2-close-btn');
    if (btn) btn.addEventListener('click', closeDrawer);
  }

  var ICON_SAVE = renderStamIcon('save', { className: 'is-xs' });
  var ICON_PLUS = renderStamIcon('plus', { className: 'is-xs' });
  var ICON_EDIT = renderStamIcon('edit', { className: 'is-xs' });

  // ── 푸터 렌더링 — 요구사항정의서 Drawer footer 패턴 정렬 ─────────────────
  function renderFooter(mode, rec) {
    var foot = $('msv2-foot');
    if (!foot) return;
    if (mode === 'detail') {
      foot.innerHTML =
        '<div class="stam-dw-foot-meta"><span>최종 수정: ' + esc(dpart(rec && rec.updatedAt)) + '</span></div>' +
        '<div class="stam-dw-foot-spacer"></div>' +
        '<div class="stam-dw-foot-right">' +
          '<button class="stam-btn stam-btn-danger-outline stam-btn--md stam-btn--danger-outline" id="msv2-del-btn"  type="button">삭제</button>' +
          '<button class="stam-btn stam-btn-ghost-muted stam-btn--md stam-btn--ghost-muted" id="msv2-view-btn" type="button">전체 보기</button>' +
          '<button class="stam-btn stam-btn-primary stam-btn--md stam-btn--primary" id="msv2-edit-btn" type="button">' + ICON_EDIT + ' 수정</button>' +
        '</div>';
      var delBtn  = $('msv2-del-btn');  if (delBtn)  delBtn.addEventListener('click',  del);
      var editBtn = $('msv2-edit-btn'); if (editBtn) editBtn.addEventListener('click',  function () { openEdit(currentId); });
    } else {
      var submitLabel = mode === 'register' ? '등록' : '저장';
      var submitIcon  = mode === 'register' ? ICON_PLUS : ICON_SAVE;
      foot.innerHTML =
        '<div class="stam-dw-foot-left">' +
          '<button class="stam-btn stam-btn-ghost stam-btn--md stam-btn--ghost" id="msv2-cancel-btn" type="button">취소</button>' +
          '<button class="stam-btn stam-btn-outline stam-btn--md stam-btn--secondary" id="msv2-draft-btn" type="button">' + ICON_SAVE + ' 임시저장</button>' +
          '<button class="stam-btn stam-btn-ghost stam-btn--md stam-btn--ghost" id="msv2-view-btn" type="button">전체 보기</button>' +
        '</div>' +
        '<div class="stam-dw-foot-spacer"></div>' +
        '<div class="stam-dw-foot-right">' +
          '<button class="stam-btn stam-btn-primary stam-btn--md stam-btn--primary" id="msv2-save-btn" type="button">' + submitIcon + ' ' + submitLabel + '</button>' +
        '</div>';
      var cancelBtn = $('msv2-cancel-btn'); if (cancelBtn) cancelBtn.addEventListener('click', closeDrawer);
      var saveBtn   = $('msv2-save-btn');   if (saveBtn)   saveBtn.addEventListener('click',   save);
    }
  }

  // ── 폼 필드 ID 매핑 ───────────────────────────────────────────────────────
  var FMAP = {
    screenName:            'msv2-f-name',
    menuName:              'msv2-f-menuname',
    lv1:                   'msv2-f-lv1',
    lv2:                   'msv2-f-lv2',
    lv3:                   'msv2-f-lv3',
    screenType:            'msv2-f-type',
    channel:               'msv2-f-channel',
    routePath:             'msv2-f-route',
    owner:                 'msv2-f-owner',
    status:                'msv2-f-status',
    reviewStatus:          'msv2-f-review',
    requirementId:         'msv2-f-req',
    functionId:            'msv2-f-func',
    screenSpecificationId: 'msv2-f-spec',
    description:           'msv2-f-desc',
  };

  function fillForm(rec) {
    Object.keys(FMAP).forEach(function (k) {
      var el = $(FMAP[k]);
      if (!el) return;
      el.value = (rec && rec[k] != null) ? rec[k] : '';
    });
  }

  function readForm() {
    var f = {};
    Object.keys(FMAP).forEach(function (k) {
      var el = $(FMAP[k]);
      if (el) f[k] = el.value.trim();
    });
    return f;
  }

  // ── Drawer 열기 ──────────────────────────────────────────────────────────
  function openRegister() {
    currentId = null;
    resetSelects();
    fillForm(null);
    setHeader(null, 'register');
    renderFooter('register', null);
    openDrawer('register');
    enhanceSelects();
  }

  function openDetail(id) {
    var d = db(); if (!d) return;
    d.getRecord(STORE, id).then(function (rec) {
      if (!rec) return;
      currentId = id;
      setHeader(rec, 'detail');
      renderDetail(rec);
      renderFooter('detail', rec);
      openDrawer('detail');
    });
  }

  function openEdit(id) {
    var d = db(); if (!d) return;
    d.getRecord(STORE, id).then(function (rec) {
      if (!rec) return;
      currentId = id;
      resetSelects();
      fillForm(rec);
      setHeader(rec, 'edit');
      renderFooter('edit', rec);
      openDrawer('edit');
      enhanceSelects();
    });
  }

  // ── CRUD ──────────────────────────────────────────────────────────────────
  function save() {
    var d = db(); if (!d) return;
    var f = readForm();
    if (!f.screenName) { alert('화면명을 입력하세요.'); return; }

    if (currentId) {
      var id    = currentId;
      var patch = Object.assign({}, f, { updatedAt: nowIso() });
      d.updateRecord(STORE, id, patch)
        .then(function () {
          return d.appendChange({ changeId: changeId(id, 'update'), projectId: PID, artifactId: id,
            changeType: 'update', field: 'menuScreen', before: null, after: f.screenName, at: nowIso(), by: BY });
        })
        .then(function () { closeDrawer(); return refresh(); })
        .catch(function (e) { alert('저장 오류: ' + e.message); });
    } else {
      var now  = nowIso();
      var nid  = 'MENU-MANUAL-' + now.slice(0, 10).replace(/-/g, '') + '-' + now.slice(11, 19).replace(/:/g, '');
      var rec  = Object.assign({
        id: nid, projectId: PID, sourceType: 'manual',
        status: f.status || 'draft', reviewStatus: f.reviewStatus || 'Review Needed',
        createdAt: now, updatedAt: now
      }, f);
      d.createRecord(STORE, rec)
        .then(function () {
          return d.appendChange({ changeId: changeId(nid, 'create'), projectId: PID, artifactId: nid,
            changeType: 'create', field: 'menuScreen', before: null, after: f.screenName, at: now, by: BY });
        })
        .then(function () { closeDrawer(); return refresh(); })
        .catch(function (e) { alert('등록 오류: ' + e.message); });
    }
  }

  function del() {
    if (!currentId) return;
    if (!confirm('이 메뉴/화면을 삭제하시겠습니까?')) return;
    var d = db(); if (!d) return;
    var id = currentId;
    d.softDeleteRecord(STORE, id, { by: BY, reason: '사용자 삭제' })
      .then(function () {
        return d.appendChange({ changeId: changeId(id, 'delete'), projectId: PID, artifactId: id,
          changeType: 'delete', field: 'status', before: 'active', after: 'deleted', at: nowIso(), by: BY });
      })
      .then(function () { closeDrawer(); return refresh(); })
      .catch(function (e) { alert('삭제 오류: ' + e.message); });
  }

  function refresh() {
    if (window.STAM && window.STAM.mslv2 && window.STAM.mslv2.refresh) {
      return window.STAM.mslv2.refresh();
    }
    return Promise.resolve();
  }

  // ── 이벤트 바인딩 ────────────────────────────────────────────────────────
  // data-msv2-reg: 헤더 버튼 포함 어디서든 직접 등록 열기 (캡처 위임)
  document.addEventListener('click', function (e) {
    var reg = e.target.closest('[data-msv2-reg]');
    if (reg) { e.stopPropagation(); openRegister(); return; }
  }, true);

  // tbody: v2 행 클릭 → 상세/수정
  // stopImmediatePropagation으로 STAMBoardList의 table 버블 핸들러가 반응하지 않게 막음
  var tbody = $('msl-tbody');
  if (tbody) tbody.addEventListener('click', function (e) {
    var row = e.target.closest('.stam-board-data-row');
    if (!row) return;                              // v2 행이 아니면 기존 핸들러에 위임
    e.stopPropagation();
    e.stopImmediatePropagation();
    var ed = e.target.closest('[data-msv2-edit]');
    if (ed)  { openEdit(ed.getAttribute('data-msv2-edit'));     return; }
    var de = e.target.closest('[data-msv2-detail]');
    if (de)  { openDetail(de.getAttribute('data-msv2-detail')); return; }
    var id = row.getAttribute('data-msv2-id');
    if (id) openDetail(id);
  });

  // scrim 클릭 → 닫기
  if (scrim) scrim.addEventListener('click', closeDrawer);

  // 탭 전환
  var tabsEl = $('msv2-tabs');
  if (tabsEl) tabsEl.addEventListener('click', function (e) {
    var t = e.target.closest('.stam-drawer-tab'); if (!t) return;
    setActiveTab(t.getAttribute('data-msv2-tab'));
  });

  window.STAM = window.STAM || {};
  window.STAM.mslCrud = { openDetail: openDetail, openEdit: openEdit, openRegister: openRegister };

}());
