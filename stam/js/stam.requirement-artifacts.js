/* ============================================================================
 * STAM Requirement Related Artifacts — 공통 생성 로직 SSOT (v0.1)
 * ----------------------------------------------------------------------------
 * 요구사항 직접 등록 · 엑셀 일괄 등록에서 공유하는 「관련 산출물 초안 함께
 * 만들기」 순수 payload builder. DOM·화면·DB에 의존하지 않는다.
 *
 *   - buildRelatedArtifactsFromRequirement — record/link/change payload 생성
 *   - generateRelatedArtifactsFromRequirement — build 결과를 Promise로 반환하는 wrapper
 *
 * PR #277 설계(§5–§9) 기준. 테스트 시나리오 자동 생성은 범위 밖.
 * 후속 PR에서 requirements-crud · import-app 이 본 모듈을 호출한다.
 * ==========================================================================*/
(function () {
  'use strict';

  var BY = 'prototype-user';
  var WBS_PHASES = ['분석', '설계', '검증'];
  var SKIP_MESSAGE = '이미 생성된 관련 산출물이 있어 누락된 항목만 추가했습니다.';

  var DEFAULT_TARGETS = {
    menuScreen: true,
    wbs: true,
    screenSpecification: false,
    functionalDefinition: false
  };

  function nowIso() { return new Date().toISOString(); }
  function pad(n) { return n < 10 ? '0' + n : '' + n; }

  function makeGenerationBatchId() {
    var d = new Date();
    return 'REQ-GEN-' +
      d.getFullYear() + pad(d.getMonth() + 1) + pad(d.getDate()) + '-' +
      pad(d.getHours()) + pad(d.getMinutes()) + pad(d.getSeconds());
  }

  function changeId(artifactId, kind) {
    return 'CHG-' + artifactId + '-' + kind + '-' + Date.now();
  }

  function linkId(prefix, batchId, seq) {
    return prefix + '-' + (batchId || 'manual') + '-' + seq;
  }

  function copyTargets(targets) {
    return {
      menuScreen: !!targets.menuScreen,
      wbs: !!targets.wbs,
      screenSpecification: !!targets.screenSpecification,
      functionalDefinition: !!targets.functionalDefinition
    };
  }

  function normalizeOptions(options) {
    var raw = options || {};
    var createRelatedArtifacts = raw.createRelatedArtifacts !== false;
    var targets = copyTargets(Object.assign({}, DEFAULT_TARGETS, raw.targets || {}));
    if (!createRelatedArtifacts) {
      targets = { menuScreen: false, wbs: false, screenSpecification: false, functionalDefinition: false };
    }
    var flow = raw.generatedByFlow === 'excel' ? 'excel' : 'manual';
    return {
      createRelatedArtifacts: createRelatedArtifacts,
      targets: targets,
      generatedByFlow: flow,
      generationBatchId: raw.generationBatchId || makeGenerationBatchId(),
      projectId: raw.projectId || '',
      importRowId: raw.importRowId || '',
      sourceRef: raw.sourceRef || '',
      createdBy: raw.by || raw.createdBy || BY,
      existing: raw.existing || {}
    };
  }

  function resolveRequirementId(requirement) {
    if (!requirement) return 'REQ-TEMP-' + Date.now();
    return requirement.id || requirement.requirementId || ('REQ-TEMP-' + Date.now());
  }

  function reqTitle(requirement) {
    if (!requirement) return '(요구사항)';
    return requirement.title || requirement.requirementTitle || '(요구사항)';
  }

  function resolveProjectId(requirement, options) {
    return options.projectId || (requirement && requirement.projectId) || '';
  }

  function resolveSourceType(flow) {
    return flow === 'excel' ? 'Requirement Import' : 'Requirement Manual';
  }

  function buildMetadata(requirement, options, reqId) {
    return {
      sourceRequirementId: reqId,
      sourceRequirementNo: (requirement && requirement.requirementId) || reqId,
      sourceRequirementTitle: reqTitle(requirement),
      generatedFrom: 'requirement',
      generatedByFlow: options.generatedByFlow,
      autoGenerated: true,
      generationBatchId: options.generationBatchId || '',
      importBatchId: options.generationBatchId || '',
      importRowId: options.importRowId || reqId,
      sourceType: resolveSourceType(options.generatedByFlow),
      sourceRef: options.sourceRef || reqId,
      requirementId: reqId
    };
  }

  function baseRecord(requirement, options, reqId, extra) {
    var t = nowIso();
    var meta = buildMetadata(requirement, options, reqId);
    var rec = {
      projectId: resolveProjectId(requirement, options),
      status: 'draft',
      reviewStatus: 'Review Needed',
      owner: (requirement && requirement.owner) || '미지정',
      description: (requirement && requirement.description) || '',
      priority: (requirement && requirement.priority) || '',
      actor: (requirement && requirement.actor) || '',
      requirementType: (requirement && requirement.requirementType) || '',
      sourceNote: (requirement && requirement.sourceNote) || '',
      createdAt: t,
      updatedAt: t,
      createdBy: options.createdBy,
      updatedBy: options.createdBy
    };
    Object.keys(meta).forEach(function (k) { rec[k] = meta[k]; });
    if (extra) Object.keys(extra).forEach(function (k) { rec[k] = extra[k]; });
    return rec;
  }

  function buildFunctionalDefinition(requirement, options, reqId) {
    var title = reqTitle(requirement);
    return {
      store: 'functionalDefinitions',
      target: 'functionalDefinition',
      rec: baseRecord(requirement, options, reqId, {
        id: reqId + '-FUN',
        boardType: 'functionalDefinition',
        title: '[기능정의 초안] ' + title,
        description: '요구사항 "' + title + '"을 기준으로 기능정의 초안 작성이 필요합니다.',
        functionType: '미지정'
      })
    };
  }

  function buildMenuScreen(requirement, options, reqId) {
    var title = reqTitle(requirement);
    return {
      store: 'menuScreens',
      target: 'menuScreen',
      rec: baseRecord(requirement, options, reqId, {
        id: reqId + '-SCR',
        boardType: 'menuScreen',
        title: '[화면 후보] ' + title,
        screenName: title || '(요구사항 기반 화면 초안)',
        menuName: title || '(요구사항 기반 메뉴 초안)',
        lv1: '',
        lv2: '',
        lv3: '',
        screenType: '화면',
        channel: '',
        routePath: '',
        functionId: '',
        screenSpecificationId: '',
        description: (requirement && requirement.description) || '요구사항/기능정의 기준 화면 후보 초안.'
      })
    };
  }

  function buildWbsItems(requirement, options, reqId) {
    var title = reqTitle(requirement);
    return WBS_PHASES.map(function (phase, i) {
      var id = reqId + '-WBS' + (i + 1);
      var wbsTitle = '[WBS 초안] ' + title + ' ' + phase;
      return {
        store: 'wbsItems',
        target: 'wbs',
        rec: baseRecord(requirement, options, reqId, {
          id: id,
          boardType: 'wbs',
          wbsId: id,
          title: wbsTitle,
          taskName: wbsTitle,
          taskType: phase,
          functionGroup: '',
          menuPath: '',
          assignee: (requirement && requirement.owner) || '미지정',
          reviewer: '',
          startDate: '',
          endDate: '',
          effortEstimate: '',
          progress: 0,
          functionId: '',
          screenId: '',
          parentWbsId: '',
          description: phase + ' 작업 초안.'
        })
      };
    });
  }

  function buildScreenSpecification(requirement, options, reqId) {
    var title = reqTitle(requirement);
    return {
      store: 'screenSpecifications',
      target: 'screenSpecification',
      rec: baseRecord(requirement, options, reqId, {
        id: reqId + '-SSP',
        boardType: 'screenSpecification',
        title: '[화면설계 초안] ' + title,
        description: '화면설계서 초안.',
        templateId: '',
        screenName: title,
        screenType: '화면'
      })
    };
  }

  function applyExistingSkips(targets, existing) {
    var effectiveTargets = copyTargets(targets);
    var skipped = [];

    if (existing.menuScreen === true && effectiveTargets.menuScreen) {
      effectiveTargets.menuScreen = false;
      skipped.push({ target: 'menuScreen', reason: 'already_exists' });
    }
    if (existing.functionalDefinition === true && effectiveTargets.functionalDefinition) {
      effectiveTargets.functionalDefinition = false;
      skipped.push({ target: 'functionalDefinition', reason: 'already_exists' });
    }
    if (existing.screenSpecification === true && effectiveTargets.screenSpecification) {
      effectiveTargets.screenSpecification = false;
      skipped.push({ target: 'screenSpecification', reason: 'already_exists' });
    }
    if (existing.wbs === true && effectiveTargets.wbs) {
      effectiveTargets.wbs = false;
      skipped.push({ target: 'wbs', reason: 'already_exists' });
    }

    return { effectiveTargets: effectiveTargets, skipped: skipped };
  }

  function toCreatedItem(item) {
    return {
      target: item.target,
      store: item.store,
      id: item.rec.id,
      record: item.rec
    };
  }

  function buildLinks(reqId, options, created) {
    var projectId = options.projectId || '';
    var batchId = options.generationBatchId || 'manual';
    var seq = 0;
    function nextId() { seq += 1; return linkId('LNK', batchId, seq); }
    function findId(target) {
      for (var i = 0; i < created.length; i++) {
        if (created[i].target === target) return created[i].id;
      }
      return null;
    }

    var funId = findId('functionalDefinition');
    var scrId = findId('menuScreen');
    var sspId = findId('screenSpecification');
    var links = [];
    var t = nowIso();

    function pushLink(fromId, toId, fromType, toType, linkType) {
      if (!fromId || !toId) return;
      links.push({
        linkId: nextId(),
        projectId: projectId,
        fromArtifactId: fromId,
        toArtifactId: toId,
        fromType: fromType,
        toType: toType,
        linkType: linkType,
        linkStatus: 'reviewNeeded',
        importBatchId: batchId,
        requirementId: reqId,
        importRowId: options.importRowId || reqId,
        createdAt: t,
        createdBy: options.createdBy
      });
    }

    pushLink(reqId, funId, 'requirement', 'functionalDefinition', 'requirementToFunction');
    pushLink(funId, scrId, 'functionalDefinition', 'menuScreen', 'functionToScreen');
    pushLink(scrId, sspId, 'menuScreen', 'screenSpecification', 'screenToScreenSpec');
    created.forEach(function (item) {
      if (item.target === 'wbs') {
        pushLink(scrId, item.id, 'menuScreen', 'wbs', 'screenToWbs');
      }
    });
    return links;
  }

  function buildChanges(options, created, links) {
    var projectId = options.projectId || '';
    var batchId = options.generationBatchId || '';
    var changes = [];

    created.forEach(function (item) {
      changes.push({
        changeId: changeId(item.id, 'create'),
        projectId: projectId,
        artifactId: item.id,
        changeType: 'create',
        field: item.target,
        before: null,
        after: item.record.title,
        importBatchId: batchId,
        at: nowIso(),
        by: options.createdBy
      });
    });

    links.forEach(function (l) {
      changes.push({
        changeId: changeId(l.fromArtifactId, 'link-' + l.linkType),
        projectId: projectId,
        artifactId: l.fromArtifactId,
        changeType: 'link',
        field: l.linkType,
        before: null,
        after: l.toArtifactId,
        importBatchId: batchId,
        at: nowIso(),
        by: options.createdBy
      });
    });
    return changes;
  }

  function buildRelatedArtifactsFromRequirement(requirement, options) {
    var normalized = normalizeOptions(options);
    var reqId = resolveRequirementId(requirement);
    var empty = {
      requirementId: reqId,
      created: [],
      skipped: [],
      links: [],
      changes: [],
      message: ''
    };

    if (!normalized.createRelatedArtifacts) {
      return empty;
    }

    if (!normalized.projectId && requirement && requirement.projectId) {
      normalized.projectId = requirement.projectId;
    }

    var skipInfo = applyExistingSkips(normalized.targets, normalized.existing);
    var built = [];

    if (skipInfo.effectiveTargets.functionalDefinition) {
      built.push(buildFunctionalDefinition(requirement, normalized, reqId));
    }
    if (skipInfo.effectiveTargets.menuScreen) {
      built.push(buildMenuScreen(requirement, normalized, reqId));
    }
    if (skipInfo.effectiveTargets.wbs) {
      built = built.concat(buildWbsItems(requirement, normalized, reqId));
    }
    if (skipInfo.effectiveTargets.screenSpecification) {
      built.push(buildScreenSpecification(requirement, normalized, reqId));
    }

    var created = built.map(toCreatedItem);
    var links = buildLinks(reqId, normalized, created);
    var changes = buildChanges(normalized, created, links);

    return {
      requirementId: reqId,
      created: created,
      skipped: skipInfo.skipped,
      links: links,
      changes: changes,
      message: skipInfo.skipped.length ? SKIP_MESSAGE : ''
    };
  }

  function generateRelatedArtifactsFromRequirement(requirement, options) {
    return Promise.resolve(buildRelatedArtifactsFromRequirement(requirement, options));
  }

  window.STAM = window.STAM || {};
  if (window.STAM.requirementArtifacts) return;

  window.STAM.requirementArtifacts = {
    DEFAULT_TARGETS: copyTargets(DEFAULT_TARGETS),
    normalizeOptions: normalizeOptions,
    buildRelatedArtifactsFromRequirement: buildRelatedArtifactsFromRequirement,
    generateRelatedArtifactsFromRequirement: generateRelatedArtifactsFromRequirement
  };
}());
