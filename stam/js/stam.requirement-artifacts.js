/* ============================================================================
 * STAM Requirement Related Artifacts — 공통 생성 로직 SSOT (v0.1)
 * ----------------------------------------------------------------------------
 * 요구사항 직접 등록 · 엑셀 일괄 등록에서 공유하는 「관련 산출물 초안 함께
 * 만들기」 순수/저장 로직. DOM·화면에 의존하지 않는다.
 *
 *   - buildRelatedArtifactsFromRequirement — DB 없이 record/link/change 생성
 *   - generateRelatedArtifactsFromRequirement — 중복 방지 후 v2 store 저장
 *
 * PR #277 설계(§5–§9) 기준. 테스트 시나리오 자동 생성은 범위 밖.
 * 후속 PR에서 requirements-crud · cycle-db.import-app 이 본 모듈을 호출한다.
 * ==========================================================================*/
(function () {
  'use strict';

  var BY = 'prototype-user';
  var WBS_PHASES = ['분석', '설계', '검증'];
  var SKIP_MESSAGE = '이미 생성된 관련 산출물이 있어 누락된 항목만 추가했습니다.';

  var DEFAULT_TARGETS = {
    menuScreen: true,
    wbs: true,
    screenSpecification: false,
    functionalDefinition: false
  };

  function nowIso() { return new Date().toISOString(); }
  function pad(n) { return n < 10 ? '0' + n : '' + n; }

  function changeId(artifactId, kind) {
    return 'CHG-' + artifactId + '-' + kind + '-' + Date.now();
  }

  function linkId(prefix, batchId, seq) {
    return prefix + '-' + (batchId || 'manual') + '-' + seq;
  }

  function copyTargets(targets) {
    return {
      menuScreen: !!targets.menuScreen,
      wbs: !!targets.wbs,
      screenSpecification: !!targets.screenSpecification,
      functionalDefinition: !!targets.functionalDefinition
    };
  }

  function normalizeOptions(options) {
    var raw = options || {};
    var createRelatedArtifacts = raw.createRelatedArtifacts === true;
    var targets = copyTargets(Object.assign({}, DEFAULT_TARGETS, raw.targets || {}));
    if (!createRelatedArtifacts) {
      targets = { menuScreen: false, wbs: false, screenSpecification: false, functionalDefinition: false };
    }
    var flow = raw.generatedByFlow === 'excel' ? 'excel' : 'manual';
    return {
      createRelatedArtifacts: createRelatedArtifacts,
      targets: targets,
      generatedByFlow: flow,
      generationBatchId: raw.generationBatchId || (flow === 'manual' ? 'REQ-MANUAL' : ''),
      projectId: raw.projectId || '',
      importRowId: raw.importRowId || '',
      sourceRef: raw.sourceRef || '',
      createdBy: raw.createdBy || BY
    };
  }

  function reqTitle(requirement) {
    return (requirement && (requirement.title || requirement.requirementTitle)) || '(요구사항)';
  }

  function buildMetadata(requirement, options) {
    var reqId = requirement.id;
    return {
      sourceRequirementId: reqId,
      sourceRequirementNo: requirement.requirementId || reqId,
      sourceRequirementTitle: reqTitle(requirement),
      generatedFrom: 'requirement',
      generatedByFlow: options.generatedByFlow,
      autoGenerated: true,
      generationBatchId: options.generationBatchId || '',
      importBatchId: options.generationBatchId || (options.generatedByFlow === 'manual' ? 'REQ-MANUAL' : ''),
      importRowId: options.importRowId || reqId,
      sourceType: 'Requirement Import',
      sourceRef: options.sourceRef || reqId,
      requirementId: reqId
    };
  }

  function baseRecord(requirement, options, extra) {
    var t = nowIso();
    var meta = buildMetadata(requirement, options);
    var rec = {
      projectId: options.projectId || requirement.projectId || '',
      status: 'draft',
      reviewStatus: 'Review Needed',
      owner: (requirement && requirement.owner) || '미지정',
      description: (requirement && requirement.description) || '',
      priority: (requirement && requirement.priority) || '',
      actor: (requirement && requirement.actor) || '',
      requirementType: (requirement && requirement.requirementType) || '',
      sourceNote: (requirement && requirement.sourceNote) || '',
      createdAt: t,
      updatedAt: t,
      createdBy: options.createdBy,
      updatedBy: options.createdBy
    };
    Object.keys(meta).forEach(function (k) { rec[k] = meta[k]; });
    if (extra) Object.keys(extra).forEach(function (k) { rec[k] = extra[k]; });
    return rec;
  }

  function buildFunctionalDefinition(requirement, options) {
    var reqId = requirement.id;
    var title = reqTitle(requirement);
    return {
      store: 'functionalDefinitions',
      target: 'functionalDefinition',
      rec: baseRecord(requirement, options, {
        id: reqId + '-FUN',
        boardType: 'functionalDefinition',
        title: '[기능정의 초안] ' + title,
        description: '요구사항 "' + title + '"을 기준으로 기능정의 초안 작성이 필요합니다.',
        functionType: '미지정'
      })
    };
  }

  function buildMenuScreen(requirement, options) {
    var reqId = requirement.id;
    var title = reqTitle(requirement);
    return {
      store: 'menuScreens',
      target: 'menuScreen',
      rec: baseRecord(requirement, options, {
        id: reqId + '-SCR',
        boardType: 'menuScreen',
        title: '[화면 후보] ' + title,
        screenName: title || '(요구사항 기반 화면 초안)',
        menuName: title || '(요구사항 기반 메뉴 초안)',
        lv1: '',
        lv2: '',
        lv3: '',
        screenType: '화면',
        channel: '',
        routePath: '',
        functionId: '',
        screenSpecificationId: '',
        description: (requirement && requirement.description) || '요구사항/기능정의 기준 화면 후보 초안.'
      })
    };
  }

  function buildWbsItems(requirement, options) {
    var reqId = requirement.id;
    var title = reqTitle(requirement);
    return WBS_PHASES.map(function (phase, i) {
      var id = reqId + '-WBS' + (i + 1);
      var wbsTitle = '[WBS 초안] ' + title + ' ' + phase;
      return {
        store: 'wbsItems',
        target: 'wbs',
        rec: baseRecord(requirement, options, {
          id: id,
          boardType: 'wbs',
          wbsId: id,
          title: wbsTitle,
          taskName: wbsTitle,
          taskType: phase,
          functionGroup: '',
          menuPath: '',
          assignee: (requirement && requirement.owner) || '미지정',
          reviewer: '',
          startDate: '',
          endDate: '',
          effortEstimate: '',
          progress: 0,
          functionId: '',
          screenId: '',
          parentWbsId: '',
          description: phase + ' 작업 초안.'
        })
      };
    });
  }

  function buildScreenSpecification(requirement, options) {
    var reqId = requirement.id;
    var title = reqTitle(requirement);
    return {
      store: 'screenSpecifications',
      target: 'screenSpecification',
      rec: baseRecord(requirement, options, {
        id: reqId + '-SSP',
        boardType: 'screenSpecification',
        title: '[화면설계 초안] ' + title,
        description: '화면설계서 초안.',
        templateId: '',
        screenName: title,
        screenType: '화면'
      })
    };
  }

  function buildLinks(requirement, options, built) {
    var reqId = requirement.id;
    var projectId = options.projectId || requirement.projectId || '';
    var batchId = options.generationBatchId || 'manual';
    var seq = 0;
    function nextId() { seq += 1; return linkId('LNK', batchId, seq); }
    function findTarget(target) {
      for (var i = 0; i < built.length; i++) {
        if (built[i].target === target) return built[i].rec.id;
      }
      return null;
    }

    var funId = findTarget('functionalDefinition');
    var scrId = findTarget('menuScreen');
    var sspId = findTarget('screenSpecification');
    var links = [];
    var t = nowIso();

    function pushLink(fromId, toId, fromType, toType, linkType) {
      if (!fromId || !toId) return;
      links.push({
        linkId: nextId(),
        projectId: projectId,
        fromArtifactId: fromId,
        toArtifactId: toId,
        fromType: fromType,
        toType: toType,
        linkType: linkType,
        linkStatus: 'reviewNeeded',
        importBatchId: batchId,
        requirementId: reqId,
        importRowId: options.importRowId || reqId,
        createdAt: t,
        createdBy: options.createdBy
      });
    }

    pushLink(reqId, funId, 'requirement', 'functionalDefinition', 'requirementToFunction');
    pushLink(funId, scrId, 'functionalDefinition', 'menuScreen', 'functionToScreen');
    pushLink(scrId, sspId, 'menuScreen', 'screenSpecification', 'screenToScreenSpec');
    built.forEach(function (item) {
      if (item.target === 'wbs') {
        pushLink(scrId, item.rec.id, 'menuScreen', 'wbs', 'screenToWbs');
      }
    });
    return links;
  }

  function buildChanges(options, built, links) {
    var projectId = options.projectId || '';
    var batchId = options.generationBatchId || '';
    var changes = [];

    built.forEach(function (item) {
      changes.push({
        changeId: changeId(item.rec.id, 'create'),
        projectId: projectId,
        artifactId: item.rec.id,
        changeType: 'create',
        field: item.target,
        before: null,
        after: item.rec.title,
        importBatchId: batchId,
        at: nowIso(),
        by: options.createdBy
      });
    });

    links.forEach(function (l) {
      changes.push({
        changeId: changeId(l.fromArtifactId, 'link-' + l.linkType),
        projectId: projectId,
        artifactId: l.fromArtifactId,
        changeType: 'link',
        field: l.linkType,
        before: null,
        after: l.toArtifactId,
        importBatchId: batchId,
        at: nowIso(),
        by: options.createdBy
      });
    });
    return changes;
  }

  function buildRelatedArtifactsFromRequirement(requirement, options) {
    var normalized = normalizeOptions(options);
    var built = [];
    if (!requirement || !requirement.id || !normalized.createRelatedArtifacts) {
      return { records: [], links: [], changes: [], targets: normalized.targets };
    }
    if (!normalized.projectId && requirement.projectId) {
      normalized.projectId = requirement.projectId;
    }

    if (normalized.targets.functionalDefinition) {
      built.push(buildFunctionalDefinition(requirement, normalized));
    }
    if (normalized.targets.menuScreen) {
      built.push(buildMenuScreen(requirement, normalized));
    }
    if (normalized.targets.wbs) {
      built = built.concat(buildWbsItems(requirement, normalized));
    }
    if (normalized.targets.screenSpecification) {
      built.push(buildScreenSpecification(requirement, normalized));
    }

    var records = built.map(function (item) {
      return { store: item.store, rec: item.rec, target: item.target };
    });
    var links = buildLinks(requirement, normalized, built);
    var changes = buildChanges(normalized, built, links);
    return {
      records: records,
      links: links,
      changes: changes,
      targets: normalized.targets
    };
  }

  function hasMenuScreen(items, reqId) {
    return (items || []).some(function (m) {
      return m.requirementId === reqId || m.sourceRef === reqId;
    });
  }

  function hasStoreMatch(items, reqId) {
    return (items || []).some(function (item) {
      return item.requirementId === reqId || item.sourceRef === reqId;
    });
  }

  function resolveSkipTargets(requirement, options, existingByStore) {
    var reqId = requirement.id;
    var skipped = [];
    var effectiveTargets = copyTargets(options.targets);

    if (effectiveTargets.menuScreen && hasMenuScreen(existingByStore.menuScreens, reqId)) {
      effectiveTargets.menuScreen = false;
      skipped.push({ target: 'menuScreen', reason: 'already_exists' });
    }
    if (effectiveTargets.functionalDefinition && hasStoreMatch(existingByStore.functionalDefinitions, reqId)) {
      effectiveTargets.functionalDefinition = false;
      skipped.push({ target: 'functionalDefinition', reason: 'already_exists' });
    }
    if (effectiveTargets.screenSpecification && hasStoreMatch(existingByStore.screenSpecifications, reqId)) {
      effectiveTargets.screenSpecification = false;
      skipped.push({ target: 'screenSpecification', reason: 'already_exists' });
    }
    if (effectiveTargets.wbs && hasStoreMatch(existingByStore.wbsItems, reqId)) {
      effectiveTargets.wbs = false;
      skipped.push({ target: 'wbs', reason: 'already_exists' });
    }

    return { effectiveTargets: effectiveTargets, skipped: skipped };
  }

  function loadExistingRecords(db, projectId) {
    var stores = ['menuScreens', 'wbsItems', 'screenSpecifications', 'functionalDefinitions'];
    return Promise.all(stores.map(function (store) {
      return db.listRecords(store, projectId).then(function (items) {
        var bucket = {};
        bucket[store] = items || [];
        return bucket;
      });
    })).then(function (parts) {
      var merged = {};
      parts.forEach(function (part) {
        Object.keys(part).forEach(function (k) { merged[k] = part[k]; });
      });
      return merged;
    });
  }

  function persistBuilt(db, built) {
    var recordJobs = built.records.map(function (item) {
      return db.createRecord(item.store, item.rec).then(function () {
        return { target: item.target, id: item.rec.id, store: item.store };
      });
    });
    return Promise.all(recordJobs).then(function (created) {
      return Promise.all((built.links || []).map(function (l) { return db.saveLink(l); }))
        .then(function () {
          return Promise.all((built.changes || []).map(function (c) { return db.appendChange(c); }))
            .then(function () { return created; });
        });
    });
  }

  function generateRelatedArtifactsFromRequirement(requirement, options) {
    var normalized = normalizeOptions(options);
    if (!requirement || !requirement.id) {
      return Promise.resolve({ created: [], skipped: [], message: null });
    }
    if (!normalized.createRelatedArtifacts) {
      return Promise.resolve({ created: [], skipped: [], message: null });
    }

    var core = window.STAM_CORE || {};
    var db = core.db;
    var projectId = normalized.projectId || requirement.projectId;
    if (!db || !projectId) {
      var offline = buildRelatedArtifactsFromRequirement(requirement, normalized);
      return Promise.resolve({
        created: offline.records.map(function (item) {
          return { target: item.target, id: item.rec.id, store: item.store };
        }),
        skipped: [],
        message: null,
        preview: offline
      });
    }

    return loadExistingRecords(db, projectId).then(function (existingByStore) {
      var skipInfo = resolveSkipTargets(requirement, normalized, existingByStore);
      var effectiveOptions = Object.assign({}, normalized, { targets: skipInfo.effectiveTargets });
      var built = buildRelatedArtifactsFromRequirement(requirement, effectiveOptions);
      return persistBuilt(db, built).then(function (created) {
        return {
          created: created,
          skipped: skipInfo.skipped,
          message: skipInfo.skipped.length ? SKIP_MESSAGE : null
        };
      });
    });
  }

  window.STAM = window.STAM || {};
  if (window.STAM.requirementArtifacts) return;

  window.STAM.requirementArtifacts = {
    DEFAULT_TARGETS: copyTargets(DEFAULT_TARGETS),
    normalizeOptions: normalizeOptions,
    buildRelatedArtifactsFromRequirement: buildRelatedArtifactsFromRequirement,
    generateRelatedArtifactsFromRequirement: generateRelatedArtifactsFromRequirement
  };
}());
